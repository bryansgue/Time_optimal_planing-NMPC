"""
stats_summary.py — Aggregate SiL 50-trial statistics and emit LaTeX snippet.

For each circuit × controller, computes over valid trials (pos_rmse < 5 m):
  - pos_rmse:    mean ± std
  - gates_ok:    mean (integer target per circuit)
  - mean_r_err:  mean ± std  (mean gate-crossing distance)
  - solver P99:  over all valid trials concatenated

Welch's t-test compares Att vs Full for pos_rmse and mean_r_err per circuit.
Emits stats_table.tex (tabular body) ready to paste into access.tex.
"""

import os
import numpy as np
from scipy import stats

_ROOT = os.path.join(os.path.dirname(__file__), 'results')
_CIRCUITS = ('fig8', 'loop')
_MODES    = ('att', 'full')
_N_GATES  = {'fig8': 7, 'loop': 8}


def _load(circuit):
    raw = np.load(os.path.join(_ROOT, f'sil_gate_results_{circuit}.npy'),
                  allow_pickle=True).item()
    valid = [v for v in raw.values()
             if v['att']['pos_rmse'] < 5.0 and v['full']['pos_rmse'] < 5.0]
    return valid, len(raw)


def _metrics(trials, mode):
    rmse = np.array([t[mode]['pos_rmse']   for t in trials])
    gk   = np.array([t[mode]['gates_ok']   for t in trials])
    md   = np.array([t[mode]['mean_r_err'] for t in trials])
    sm   = np.concatenate([t[mode]['solver_ms'] for t in trials])
    return {'rmse': rmse, 'gates': gk, 'mean_d': md, 'solver': sm}


def _fmt(mu, sd, bold=False):
    s = f'{mu:.3f} \\pm {sd:.3f}'
    return f'$\\mathbf{{{s}}}$' if bold else f'${s}$'


def _fmt_gates(mean, total, bold=False):
    s = (f'{int(round(mean))}/{total}'
         if abs(mean - round(mean)) < 1e-6
         else f'{mean:.2f}/{total}')
    return f'$\\mathbf{{{s}}}$' if bold else f'${s}$'


def _fmt_p(mu, bold=False):
    s = f'{mu:.2f}'
    return f'$\\mathbf{{{s}}}$' if bold else f'${s}$'


def _pstr(p):
    if p < 1e-4: return '$<\\!10^{-4}$'
    if p < 1e-3: return f'${p:.1e}$'
    return f'${p:.3f}$'


def main():
    lines = []
    print('\n' + '=' * 78)
    print(f"{'Circuit':8} {'Ctrl':5} {'N':>3} {'RMSE [m]':>16} {'Gates':>10} "
          f"{'d_gate [m]':>16} {'P99 [ms]':>8}")
    print('=' * 78)

    all_stats = {}
    for circ in _CIRCUITS:
        trials, n_tot = _load(circ)
        n_valid = len(trials)
        all_stats[circ] = {}
        for mode in _MODES:
            m = _metrics(trials, mode)
            p99 = float(np.percentile(m['solver'], 99))
            all_stats[circ][mode] = {
                'n_valid': n_valid, 'n_total': n_tot,
                'rmse_mu': m['rmse'].mean(), 'rmse_sd': m['rmse'].std(ddof=1),
                'gates_mu': m['gates'].mean(),
                'md_mu': m['mean_d'].mean(), 'md_sd': m['mean_d'].std(ddof=1),
                'p99': p99,
                'rmse_raw': m['rmse'], 'md_raw': m['mean_d'],
            }
            s = all_stats[circ][mode]
            print(f"{circ:8} {mode.upper():5} {n_valid:>3} "
                  f"{s['rmse_mu']:.3f}±{s['rmse_sd']:.3f}    "
                  f"{s['gates_mu']:.2f}/{_N_GATES[circ]:d}     "
                  f"{s['md_mu']:.3f}±{s['md_sd']:.3f}   "
                  f"{s['p99']:.2f}")

        # Welch p-values
        a = all_stats[circ]['att']; f = all_stats[circ]['full']
        p_rmse = stats.ttest_ind(a['rmse_raw'], f['rmse_raw'], equal_var=False).pvalue
        p_md   = stats.ttest_ind(a['md_raw'],   f['md_raw'],   equal_var=False).pvalue
        all_stats[circ]['p_rmse'] = float(p_rmse)
        all_stats[circ]['p_md']   = float(p_md)
        print(f"         -> Welch p-values: RMSE {_pstr(p_rmse):18s}  "
              f"d_gate {_pstr(p_md)}")

    print('=' * 78)

    # Emit LaTeX tabular body
    tex = []
    tex.append('% auto-generated by experiments/stats_summary.py — do not edit by hand')
    tex.append('\\setlength{\\tabcolsep}{2.5pt}')
    tex.append('\\begin{tabular}{@{}llcccc@{}}')
    tex.append('  \\toprule')
    tex.append('  Circ. & Ctrl. & RMSE [\\si{\\m}] & Gates & $d_\\mathrm{gate}$ [\\si{\\m}] & P99 [\\si{\\ms}] \\\\')
    tex.append('  \\midrule')

    for circ in _CIRCUITS:
        a = all_stats[circ]['att']; f = all_stats[circ]['full']
        ntot = _N_GATES[circ]
        best_rmse = 'full'
        best_md   = 'full'
        best_gates = 'full' if f['gates_mu'] > a['gates_mu'] else 'att'

        tex.append(f'  \\multirow{{2}}{{*}}{{{circ.capitalize()}}}')
        # Att row
        tex.append('    & Att  & '
                   f'{_fmt(a["rmse_mu"], a["rmse_sd"], best_rmse=="att")} & '
                   f'{_fmt_gates(a["gates_mu"], ntot, best_gates=="att")} & '
                   f'{_fmt(a["md_mu"],   a["md_sd"],   best_md=="att")} & '
                   f'{_fmt_p(a["p99"])} \\\\')
        # Full row
        tex.append('    & Full & '
                   f'{_fmt(f["rmse_mu"], f["rmse_sd"], best_rmse=="full")} & '
                   f'{_fmt_gates(f["gates_mu"], ntot, best_gates=="full")} & '
                   f'{_fmt(f["md_mu"],   f["md_sd"],   best_md=="full")} & '
                   f'{_fmt_p(f["p99"])} \\\\')
        if circ != _CIRCUITS[-1]:
            tex.append('  \\midrule')
    tex.append('  \\bottomrule')
    tex.append('\\end{tabular}')

    out = '\n'.join(tex) + '\n'
    out_path = os.path.join(os.path.dirname(__file__), 'results',
                            'tab_results_sil.tex')
    with open(out_path, 'w') as fh:
        fh.write(out)
    print(f'\nLaTeX snippet written to: {out_path}\n')

    # Also emit a machine-readable summary
    import json
    summary = {c: {m: {k: (v.tolist() if isinstance(v, np.ndarray) else v)
                       for k, v in s.items() if k not in ('rmse_raw', 'md_raw')}
                   for m, s in all_stats[c].items() if m in _MODES}
               for c in _CIRCUITS}
    for c in _CIRCUITS:
        summary[c]['p_rmse'] = all_stats[c]['p_rmse']
        summary[c]['p_md']   = all_stats[c]['p_md']
    with open(os.path.join(os.path.dirname(__file__), 'results',
                           'stats_summary.json'), 'w') as fh:
        json.dump(summary, fh, indent=2)

    # Print a paragraph-ready summary for the paper
    print('=' * 78)
    print('Paragraph-ready numbers (SiL, valid trials per circuit):')
    print('=' * 78)
    for circ in _CIRCUITS:
        a = all_stats[circ]['att']; f = all_stats[circ]['full']
        ntot = _N_GATES[circ]
        rmse_red = 100 * (a['rmse_mu'] - f['rmse_mu']) / a['rmse_mu']
        md_red   = 100 * (a['md_mu']   - f['md_mu'])   / a['md_mu']
        gates_att_pct  = 100 * a['gates_mu'] / ntot
        gates_full_pct = 100 * f['gates_mu'] / ntot
        print(f'{circ.upper()}:  Att {gates_att_pct:.1f}% gates, Full {gates_full_pct:.1f}% | '
              f'RMSE reduction {rmse_red:.1f}%, d_gate reduction {md_red:.1f}% '
              f'(p_RMSE={_pstr(all_stats[circ]["p_rmse"])})')


if __name__ == '__main__':
    main()
